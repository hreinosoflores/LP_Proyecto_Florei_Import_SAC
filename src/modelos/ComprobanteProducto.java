package modelos;

public class ComprobanteProducto {
	private int num_comp, num_gui, cod_pro, cant;

	public int getNum_comp() {
		return num_comp;
	}

	public void setNum_comp(int num_comp) {
		this.num_comp = num_comp;
	}

	public int getNum_gui() {
		return num_gui;
	}

	public void setNum_gui(int num_gui) {
		this.num_gui = num_gui;
	}

	public int getCod_pro() {
		return cod_pro;
	}

	public void setCod_pro(int cod_pro) {
		this.cod_pro = cod_pro;
	}

	public int getCant() {
		return cant;
	}

	public void setCant(int cant) {
		this.cant = cant;
	}

}
